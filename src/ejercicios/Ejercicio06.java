package ejercicios;

public class Ejercicio06 {

    public static void main(String[] args) {
     
        char letra;
        
        letra ='e';
        System.out.println(letra);
        
        letra = 101;        // Decimal
        System.out.println(letra);
        
        letra = 0x65;      // Hexadecimal
        System.out.println(letra);
        
        letra = 0145;      // Octal (El número empieza por 0)
        System.out.println(letra);
        
        letra = 0b01100101; // Binario (El número empieza por 0)
        System.out.println(letra);
    }
}